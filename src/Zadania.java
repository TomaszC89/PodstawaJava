public class Zadania {
    public static void main(String[] args) {

        int celsius = 10;
        double kelvin = celsius + 273.15;

        System.out.println("Temperatura ze stopni Celsiusa na Kelviny to: " + kelvin);
        System.out.println("Temperatura w Celsiusach to: " + celsius);

        int celsius2 = 20;
        double kelvin2 = celsius2 - 273.15;

        System.out.println("Temperatura ze stopni Kelvina na Celsiusa to: " + kelvin2);
        System.out.println("Temperatura w Celsiusach to " + celsius2);

    }
}
